package Esercitazione5;

public class Contatto {
    
    int id;
    String nome;
    String cognome;
    String email;
    String telefono;

    public Contatto(int id, String nome, String cognome, String email, String telefono){
        this.id = id;
        this.nome = nome;
        this.cognome = cognome;
        this.email = email;
        this.telefono = telefono;
    }

}
